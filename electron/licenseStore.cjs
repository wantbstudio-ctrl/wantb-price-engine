function normalizeCode(code) {
  return String(code || "").trim().toUpperCase();
}

function uniqueNormalizedCodes(codes) {
  return Array.from(
    new Set(
      (Array.isArray(codes) ? codes : [])
        .map(normalizeCode)
        .filter(Boolean)
    )
  );
}

